  #[Url(history: true)]
  public $search = '';

  #[Url(history: true)]
  public $sortBy = 'centro_costos.id';

  #[Url(history: true)]
  public $sortDir = 'ASC';

  #[Url()]
  public $perPage = 10;

  public $action = 'list';
  public $recordId = '';

  public string $codigo = '';
  public string $descrip = '';
  public ?string $mcorto = null;
  public ?string $codcont = null;
  public bool $favorite = false;

  public $closeForm = false;
  public $columns;
  public $defaultColumns;
  public $listActives;

  protected $listeners = [
    'datatableSettingChange' => 'refresDatatable',
